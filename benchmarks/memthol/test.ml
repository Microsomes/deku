(*
   https://github.com/OCamlPro/memthol

   It is a visualizer and analyzer for program profiling.
   It works on memory "dumps" containing information about 
   the size and (de)allocation date of (some of) the allocations
   performed by some execution of a program

   Currently, memthol accepts memory dumps produced by Memtrace. 
   A memtrace dump for a program execution is a single Common Trace Format (ctf) file

   It is kind of a viewer for Memtrace

*)
